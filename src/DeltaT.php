    public static function deltat(float $tjd): float
    {
        return self::deltatEx($tjd, -1);
    }

    public static function deltatEx(float $tjd, int $iflag): float
    {
        $tidAcc = self::tidalAcceleration($iflag);
        $y = 2000.0 + ($tjd - self::J2000) / 365.25;

        if ($tjd < 2435108.5) {
            $dt = self::deltatStephensonEtc2016($tjd, $tidAcc);

            if ($tjd >= 2434108.5) {
                $dt += (1.0 - (2435108.5 - $tjd) / 1000.0) * 0.6610218 / 86400.0;
            }

            return $dt;
        }

        if ($y >= self::TABSTART) {
            return self::deltatAstronomicalAlmanac($tjd, $tidAcc);
        }

        return 0.0;
    }

    private static function tidalAcceleration(int $iflag): float
    {
        if ($iflag === -1) {
            return self::SE_TIDAL_DE431;
        }

        if (($iflag & self::SEFLG_MOSEPH) !== 0) {
            return self::SE_TIDAL_DE404;
        }

        return self::SE_TIDAL_DE431;
    }

    private static function deltatAstronomicalAlmanac(float $tjd, float $tidAcc): float
    {
        $dt = self::deltaTTable();
        $tabsiz = count($dt);
        $tabend = self::TABSTART + $tabsiz - 1;
        $y = 2000.0 + ($tjd - 2451544.5) / 365.25;

        if ($y <= $tabend) {
            $p = floor($y);
            $iy = (int)($p - self::TABSTART);
            $ans = $dt[$iy];

            $k = $iy + 1;
            if ($k >= $tabsiz) {
                return self::adjustForTidacc($ans, $y, $tidAcc, self::SE_TIDAL_26, false) / 86400.0;
            }

            $p = $y - $p;
            $ans += $p * ($dt[$k] - $dt[$iy]);

            if ($iy - 1 < 0 || $iy + 2 >= $tabsiz) {
                return self::adjustForTidacc($ans, $y, $tidAcc, self::SE_TIDAL_26, false) / 86400.0;
            }

            $d = [];
            $k = $iy - 2;

            for ($i = 0; $i < 5; $i++) {
                $d[$i] = ($k < 0 || $k + 1 >= $tabsiz) ? 0.0 : $dt[$k + 1] - $dt[$k];
                $k++;
            }

            for ($i = 0; $i < 4; $i++) {
                $d[$i] = $d[$i + 1] - $d[$i];
            }

            $b = 0.25 * $p * ($p - 1.0);
            $ans += $b * ($d[1] + $d[2]);

            if ($iy + 2 >= $tabsiz) {
                return self::adjustForTidacc($ans, $y, $tidAcc, self::SE_TIDAL_26, false) / 86400.0;
            }

            for ($i = 0; $i < 3; $i++) {
                $d[$i] = $d[$i + 1] - $d[$i];
            }

            $b = 2.0 * $b / 3.0;
            $ans += ($p - 0.5) * $b * $d[1];

            if ($iy - 2 < 0 || $iy + 3 > $tabsiz) {
                return self::adjustForTidacc($ans, $y, $tidAcc, self::SE_TIDAL_26, false) / 86400.0;
            }

            for ($i = 0; $i < 2; $i++) {
                $d[$i] = $d[$i + 1] - $d[$i];
            }

            $b = 0.125 * $b * ($p + 1.0) * ($p - 2.0);
            $ans += $b * ($d[0] + $d[1]);

            return self::adjustForTidacc($ans, $y, $tidAcc, self::SE_TIDAL_26, false) / 86400.0;
        }

        $b = $y - 2000.0;

        if ($y < 2500.0) {
            $ans = $b ** 3 * 121.0 / 30000000.0 + $b ** 2 / 1250.0 + $b * 521.0 / 3000.0 + 64.0;
            $b2 = $tabend - 2000.0;
            $ans2 = $b2 ** 3 * 121.0 / 30000000.0 + $b2 ** 2 / 1250.0 + $b2 * 521.0 / 3000.0 + 64.0;
        } else {
            $b = 0.01 * ($y - 2000.0);
            $ans = $b * $b * 32.5 + 42.5;
            $ans2 = $ans;
        }

        if ($y <= $tabend + 100.0) {
            $ans3 = $dt[$tabsiz - 1];
            $ans += ($ans2 - $ans3) * ($y - ($tabend + 100.0)) * 0.01;
        }

        return $ans / 86400.0;
    }

    private static function deltatStephensonEtc2016(float $tjd, float $tidAcc): float
    {
        $ygreg = 2000.0 + ($tjd - self::J2000) / 365.2425;
        $irec = null;

        foreach (self::dtcf16() as $i => $row) {
            if ($tjd < $row[0]) {
                break;
            }

            if ($tjd < $row[1]) {
                $irec = $i;
                break;
            }
        }

        if ($irec !== null) {
            $row = self::dtcf16()[$irec];
            $t = ($tjd - $row[0]) / ($row[1] - $row[0]);
            $dt = $row[2] + $row[3] * $t + $row[4] * $t * $t + $row[5] * $t * $t * $t;
        } elseif ($ygreg < -720.0) {
            $t = ($ygreg - 1825.0) / 100.0;
            $dt = -320.0 + 32.5 * $t * $t - 179.7337208;
        } else {
            $t = ($ygreg - 1825.0) / 100.0;
            $dt = -320.0 + 32.5 * $t * $t + 269.4790417;
        }

        return self::adjustForTidacc($dt, $ygreg, $tidAcc, self::SE_TIDAL_STEPHENSON_2016, true) / 86400.0;
    }

    private static function adjustForTidacc(
        float $ans,
        float $year,
        float $tidAcc,
        float $tidAcc0,
        bool  $adjustAfter1955
    ): float
    {
        if ($year < 1955.0 || $adjustAfter1955) {
            $b = $year - 1955.0;
            $ans += -0.000091 * ($tidAcc - $tidAcc0) * $b * $b;
        }

        return $ans;
    }

